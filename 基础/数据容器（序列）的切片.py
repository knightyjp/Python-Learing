# 列表、元组、字符串，都可以视为序列
# 切片："序列[起始下标:结束下标:步长]" 切片就是从原始序列中抽出的一个子序列,范围是左闭右开的 不会取到结束下标
"""
步长为1表示挨个取，为2则每次跳过一个元素取。如为负数则是反向取
切片操作并不会影响序列本身，而是会得到一个新的序列
"""
mylist = [0, 1, 2, 3, 4, 5, 6, 7, 8]
choose = mylist[0:4:2]
print(choose)
pick = mylist[:]
# 表示从头到尾挨个取
print(pick)
pick = mylist[::2]
# 表示从头到尾间隔一个取一个
print(pick)